package com;

public class Product
{
	private int Pid;
	private String Pname;
	private int Price;
	public int getPid() {
		return Pid;
	}
	public void setPid(int Pid) {
		this.Pid = Pid;
	}
	public String getPname() {
		return Pname;
	}
	public void setPname(String Pname) {
		this.Pname = Pname;
	}
	public int getPrice() {
		return Price;
	}
	public void setPrice(int Price) {
		this.Price = Price;
	}

}